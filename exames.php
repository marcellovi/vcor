<br>A Cardiologia Pediátrica é a especialidade da Pediatria que cuida das doenças do coração da criança, tanto dos problemas que nascem com a criança (Cardiopatias Congênitas) como de problemas de coração que a criança pode desenvolver ao longo da vida (Cardiopatias Adquiridas).
Em ambos os casos, o mais importante é descobrir o problema o mais cedo possível, e começar o tratamento certo. 
<br>
<br>O QUE O CARDIOPEDIATRA FAZ?
<br>
<br>O trabalho do Cardiopediatra, muitas vezes, começa ainda durante a gestação. 
Muitas Cardiopatias Congênitas (malformação cardíaca que nasce com a criança) podem ser descobertas ainda durante a gestação, através do Ultrassom Morfológico e do Ecocardiograma Fetal, exames que fazem parte do acompanhamento Pré-Natal.
Ao descobrir o problema no coração do bebê, é indicada uma consulta com o Cardiopediatra antes do nascimento, para que possam ser esclarecidas as duvidas sobre o problema, planejado o parto, o Hospital mais adequado para o bebê nascer e um planejamento dos cuidados, já que muitas vezes o bebê cardiopata precisa de atendimento especializado ao nascimento, com possíveis intervenções nas primeiras horas ou dias de vida.
